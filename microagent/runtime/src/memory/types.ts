/**
 * Memory 类型定义
 *
 * 定义记忆模块所需的类型
 */

// ============================================================================
// Memory 来源
// ============================================================================

/**
 * Memory 来源类型
 * 标识记忆条目的来源
 */
export type MemorySource = "conversation" | "preference" | "fact" | "decision";

// ============================================================================
// Memory 条目
// ============================================================================

/**
 * Memory 条目
 * 表示一条记忆记录
 */
export interface MemoryEntry {
  /** 条目唯一标识 */
  id: string;
  /** 记忆内容 */
  content: string;
  /** 记忆来源 */
  source: MemorySource;
  /** 重要性评分（0-1） */
  importance: number;
  /** 创建时间戳 */
  createdAt: number;
  /** 过期时间戳（可选） */
  expiresAt?: number;
}

// ============================================================================
// Memory 配置
// ============================================================================

/**
 * Memory 配置
 * 定义记忆模块的配置选项
 */
export interface MemoryConfig {
  /** 是否启用记忆功能 */
  enabled: boolean;
  /** 存储类型 */
  store: "file" | "sqlite";
  /** 是否自动捕获对话 */
  autoCapture: boolean;
  /** 是否自动召回记忆 */
  autoRecall: boolean;
  /** 最大返回结果数 */
  maxResults: number;
  /** 记忆过期时间（毫秒） */
  ttl?: number;
}

// ============================================================================
// Memory 搜索结果
// ============================================================================

/**
 * Memory 搜索结果
 * 表示一次搜索的单个结果
 */
export interface MemorySearchResult {
  /** 匹配的记忆条目 */
  entry: MemoryEntry;
  /** 相关性得分（0-1） */
  score: number;
}
